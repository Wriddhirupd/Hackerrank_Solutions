package Numbers;

public abstract class Number {
	
	 public abstract int sum(int a, int b, int c);
	
	 public abstract int average(int a, int b, int c);
	
	 public abstract int product(int a, int b, int c);
	
	 public abstract int smallest(int a, int b, int c);
	
	 public abstract int largest(int a, int b, int c);
}
